# Variables --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

from dotenv import load_dotenv
import os

load_dotenv()
discordToken = os.getenv("Token")

from discord.ext import commands
bot = commands.Bot(command_prefix = "nib")

@bot.command(name = "math")
async def add(context, num1, num2):
    
    result = (int(num1) + int(num2))
    await context.reply(f"{num1} + {num2} is " + str(result) + " ffs")


# Functions --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# MainSetup --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

bot.run(discordToken)
